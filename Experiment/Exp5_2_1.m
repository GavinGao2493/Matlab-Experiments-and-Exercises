clear,clc;
a1 = 0:15:90; a2 = 0:15:75;
sina = [0, 0.2588, 0.5000, 0.7071, 0.8660, 0.9659, 1.0000];
tana = [0, 0.2679, 0.5774, 1.0000, 1.7320, 3.7320];
s1 = 0:90; s2 = 0:75;

% 用3次样条插值计算0~90°内整数点的正弦值和0~75°内整数点的正切值
disp('用3次样条插值计算0~90°内整数点的正弦值：');
disp(interp1(a1, sina, s1, 'spline'));
disp('用3次样条插值计算0~75°内整数点的正切值：');
disp(interp1(a2, tana, s2, 'spline'));

% 用5次多项式拟合方法计算相同的函数值
p1 = polyfit(a1, sina, 5);
p2 = polyfit(a2, tana, 5);
disp('用5次多项式拟合方法计算0~90°内整数点的正弦值：');
disp(polyval(p1, s1));
disp('用5次多项式拟合方法计算0~75°内整数点的正切值：');
disp(polyval(p2, s2));