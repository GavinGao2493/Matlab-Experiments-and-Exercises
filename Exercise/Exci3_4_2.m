clear,clc;
a1=1:100;
a2=1:1000;
a3=1:10000;
b1=(-1).^(a1+1)./(a1*2-1);
b2=(-1).^(a2+1)./(a2*2-1);
b3=(-1).^(a3+1)./(a3*2-1);
ans1=sum(b1);
ans2=sum(b2);
ans3=sum(b3);
disp(['n=100,ans=',num2str(ans1),',pi/4=',num2str(pi/4)]);
disp(['n=1000,ans=',num2str(ans2),',pi/4=',num2str(pi/4)]);
disp(['n=10000,ans=',num2str(ans3),',pi/4=',num2str(pi/4)]);