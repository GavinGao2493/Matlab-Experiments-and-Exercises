clear,clc;
t=linspace(0,pi,1000);
x2=sin(3*t).*cos(t);
y2=sin(3*t).*sin(t);
y1=2*x2-0.5;
k=find(abs(y1-y2)<3e-3);
x3=x2(k);
y3=2*x3-0.5;
plot(x2, y1, 'b-', x2, y2, 'k:', x3, y3, 'rp', 'MarkerSize', 10);
title('两曲线交叉点');
xlabel('X');
ylabel('Y');
legend('y1', 'y2', 'y3');
text(-0.6, -2, 'y=2x-0.5');
text(-0.4, 1, 'x=sin(3t)cost');
text(-0.4, 0.8, 'y=sin(3t)sint');
text(-0.45, 0.9, '\{', FontSize=20);
text(0, 0.9, ', 0{\leq t \leq \pi}');
